
public class IntroToArrays {

	public static void main(String[] args) {
		// create an array
		String[] colors = new String[3];
		
		//store values in array
		colors[0] = "red";
		colors[1] = "yellow";
		colors[2] = "blue";
		
		//print array values
		String red = colors[0];
		
		System.out.println(red);
		System.out.println(colors[0]); 
	}

}
