
public class Odd {

	public static void main(String[] args) {
		
		//Random Integers
		
		int a=15;
		int b=346;
		int c=7135;
		int d=2368;
		int e=5679;
		
		//Functions For Remainders
		
		int A=a%2;
		int B=b%2;
		int C=c%2;
		int D=d%2;
		int E=e%2;
		
		//Function for Final # of Odds (adds all together because odds all equal 1)
		
		int Odds=A+B+C+D+E;
		
		System.out.println("Number of Odds: " + Odds);
			
		
		}

}

